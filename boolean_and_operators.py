"""
Boolean and operators
"""

like_coffee = True
like_tea = False

favorite_food = "Pizza"
favorite_number = 32

print(type(like_coffee))
print(type(favorite_food))
print(type(favorite_number))
print(like_coffee)

# Comparison operators
print(1 == 2)
print(1 != 2)
print(1 > 2)
print(1 < 2)
print(1 >= 2)
print(1 <= 2)

# Logical operators
print(like_coffee and like_tea)
print(like_coffee or like_tea)
print(not like_coffee)
